package cmm02.op;

public class No01_DanHangDemo {
	public static void main(String[] args) {
		int i = 5;
		i=i+1;
		System.out.println("i = i + 1 의 결과값 : "+i); //6
		
		int j=5;
		j++;
		System.out.println("J++ 의 결과값 : "+j); //6
		
		int k=5;
		++k;
		System.out.println("++k 의 결과값 : "+k); //6
	}
}
